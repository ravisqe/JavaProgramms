package org.example;

public class NewLine {

        public static void main(String[] args)
        {
String   s1 = "Ravi" ;
String s2 = "Sharma";
            String newline = System.lineSeparator();

            // Printing new line
            System.out.println(s1 + newline + s2);
            System.out.println(s1 + '\n' +s2);

        }
    }

